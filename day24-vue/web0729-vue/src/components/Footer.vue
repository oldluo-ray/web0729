<template>
  <div class="todo-footer">
    <label>
      <input
        type="checkbox"
        :checked="doneTotal === allTotal"
        @change="allCheckHandle"
      />
    </label>
    <span>
      <span>已完成{{ doneTotal }}</span> / 全部{{ allTotal }}
    </span>
    <button class="btn btn-danger" @click="clearDoneHandle">
      清除已完成任务
    </button>
  </div>
</template>

<script>
export default {
  props: ['list', 'allCheckHandle', 'clearDoneHandle'],
  computed: {
    allTotal() {
      return this.list.length
    },
    doneTotal() {
      return this.list.filter((item) => item.isDone).length
    },
  },
}
</script>

